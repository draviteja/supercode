package com.supercode.utils;

public class SuperCodeUtils {
    /**
     * Method to check object not null
     *
     * @param o
     * @return
     */
    public static boolean isNotNull(Object o) {
        return o != null;
    }

    /**
     * Method to check object null
     *
     * @param o
     * @return
     */
    public static boolean isNull(Object o) {
        return o == null;
    }
}
